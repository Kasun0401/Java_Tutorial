// Maths Class

public class L7_MathClass {
    public static void main(String args[]){

        System.out.println(Math.PI);
        System.out.println(Math.E);

       double result;

       result = Math.pow(2,3);   // Power 2^3 = 8
       result = Math.abs(-3);        // Absolute value
       result = Math.sqrt(25);     //Square root
       result = Math.round(3.14);  // Rounded value
       result = Math.ceil(6.21);   // Rounded up upper number
       result = Math.floor(7.98);  // Rounded up down number
       result = Math.max(12,23);
       result = Math.min(13,56);

       System.out.println(result);  
        


    }
    
}
